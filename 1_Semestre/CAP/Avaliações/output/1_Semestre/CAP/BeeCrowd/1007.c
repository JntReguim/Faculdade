// Biblioteca
#include<stdio.h>
 
// Função main
int main(){
    // Definições locais
    int a, b, c, d;
    int result;
    
    // Entrada de dados
    scanf("%d %d %d %d", &a, &b, &c, &d);
    
    // Calculando a média
    result = (a * b) - (c * d);
    
    // Impressão do restuldado
    printf("DIFERENCA = %d\n", result);
    
    return 0;
} // Fim da main